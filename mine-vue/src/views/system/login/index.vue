<script>
import { mapActions } from 'vuex'
import localeMixin from '@/locales/mixin'
import { getCaptcha } from '@/api/system/login'
export default {
  mixins: [
    localeMixin
  ],
  data () {
    return {
      captchaImg: null,
      // 表单
      formLogin: {
        username: null,
        password: null,
        code: null
      },
      // 表单校验
      rules: {
        username: [
          {
            required: true,
            message: '请输入账号',
            trigger: 'blur'
          }
        ],
        password: [
          {
            required: true,
            message: '请输入密码',
            trigger: 'blur'
          }
        ],
        code: [
          {
            required: true,
            message: '请输入验证码',
            trigger: 'blur'
          }
        ]
      }
    }
  },
  mounted () {
    this.getCaptchaImg()
  },
  methods: {
    ...mapActions('store/account', ['login']),

    getCaptchaImg () {
      getCaptcha().then(res => {
        this.captchaImg = res.data.img
      })
    },

    /**
     * @description 提交表单
     */
    submit () {
      this.$refs.loginForm.validate((valid) => {
        if (valid) {
          this.login({
            username: this.formLogin.username,
            password: this.formLogin.password,
            code: this.formLogin.code
          }).then(() => {
            this.$router.replace(this.$route.query.redirect || '/')
            this.$notify({
              title: '提示',
              message: '登录成功',
              type: 'success'
            })
          })
        }
      })
    }
  }
}
</script>

